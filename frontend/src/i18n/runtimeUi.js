const interpolate = (template, params = {}) =>
  template.replace(/\{(\w+)\}/g, (_, key) => {
    const value = params[key]
    return value == null ? `{${key}}` : String(value)
  })

export const runtimeText = (locale, key, params = {}) => {
  const base = (locale || 'en').toLowerCase().split('-')[0]
  const bundle = runtimeMessages[base] || runtimeMessages.en
  const template = bundle[key] || runtimeMessages.en[key] || key
  return interpolate(template, params)
}

export const formatLogTime = (locale, date = new Date()) => {
  const normalized = (locale || 'en').toLowerCase().split('-')[0]
  const formatter = new Intl.DateTimeFormat(
    normalized === 'zh' ? 'zh-CN' : normalized,
    {
      hour: '2-digit',
      minute: '2-digit',
      second: '2-digit',
      hour12: false,
    }
  )

  return `${formatter.format(date)}.${date.getMilliseconds().toString().padStart(3, '0')}`
}
